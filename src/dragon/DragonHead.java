package dragon;

public class DragonHead {
    private float size;
    private float toothCount;

    public DragonHead(float size, float toothCount) {
        this.size = size;
        this.toothCount = toothCount;
    }

    public float getSize() {
        return size;
    }

    public float getToothCount() {
        return toothCount;
    }
}
